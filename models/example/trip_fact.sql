with TRIPS AS (

SELECT 

RIDE_ID,
DATE(TO_TIMESTAMP(STARTED_AT)) AS TRIP_DATE,
START_STATION_ID,
END_STATION_ID,
MEMBER_CASUAL,
TIMESTAMPDIFF(SECOND, TO_TIMESTAMP(STARTED_AT), TO_TIMESTAMP(ENDED_AT)) AS TRIP_DURATION_SECONDS


FROM {{ ref('stg_bike') }}
where RIDE_ID != 'ride_id' 



)

SELECT

*

FROM TRIPS